/*
 * CONTROLADOR PI EM BASE Q
 */
int duty = 0;
int last_duty = 819;

int error = 0;
int last_error = 614;

short pi(short voltage) {
	// Fator K ajustado em Q11
	error = 3072 - (voltage * 10);

	// Novo duty cycle em Q12
	duty = last_duty + (((error * 530) - (last_duty * 480)) >> 10);

	// Duty e Erro anterior
	last_error = error;
	last_duty = duty;

	// Ajuste para 20KHz - Converte Q12 para 0 a 800
	return (short)((duty * 800) >> 12);
}

/*
 * COPIAR DA MEMÓRIA FLASH PARA A RAM
 * void* - ponteiro genérico do C (posso passar qualquer tipo de dado) para processador de 32 bits, ele tem o tamanho de 32 bits
 * size - Tamanho em bytes
 * mesmo método para processadores que não são ARM
 */
void flash2ram(void* destination, void* source, int size) {
	// Garantir que as operações sejam feitas em 1 byte (8 bits)
	unsigned char *source8 = source;
	unsigned char *destination8 = destination;

	while(size--){
		*destination8++ = *source8;
	}
}

